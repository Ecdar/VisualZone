package sample.Constraints;

public class Clock {

    protected String name;
    protected double value;

    public Clock(String name, double value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}
